void LCD::sendCmd(uint8_t cmd) {
    gpio_set_level((gpio_num_t)PIN_DC, 0);
    spi_transaction_t t = {};
    t.length = 8;
    t.tx_buffer = &cmd;
    spi_device_transmit(spi, &t);
}

void LCD::sendData(const uint8_t* data, int len) {
    if (len == 0) return;
    gpio_set_level((gpio_num_t)PIN_DC, 1);
    spi_transaction_t t = {};
    t.length = len * 8;
    t.tx_buffer = data;
    spi_device_transmit(spi, &t);
}

void LCD::setAddrWindow(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1) {
    uint8_t data[4];
    sendCmd(0x2A);
    data[0] = x0 >> 8; data[1] = x0 & 0xFF;
    data[2] = x1 >> 8; data[3] = x1 & 0xFF;
    sendData(data, 4);

    sendCmd(0x2B);
    data[0] = y0 >> 8; data[1] = y0 & 0xFF;
    data[2] = y1 >> 8; data[3] = y1 & 0xFF;
    sendData(data, 4);

    sendCmd(0x2C);
}

void LCD::init() {
    gpio_reset_pin((gpio_num_t)PIN_CS);
    gpio_set_direction((gpio_num_t)PIN_CS, GPIO_MODE_OUTPUT);
    gpio_reset_pin((gpio_num_t)PIN_DC);
    gpio_set_direction((gpio_num_t)PIN_DC, GPIO_MODE_OUTPUT);
    gpio_reset_pin((gpio_num_t)PIN_RST);
    gpio_set_direction((gpio_num_t)PIN_RST, GPIO_MODE_OUTPUT);

    spi_bus_config_t buscfg = {};
    buscfg.miso_io_num = -1;
    buscfg.mosi_io_num = PIN_MOSI;
    buscfg.sclk_io_num = PIN_CLK;
    buscfg.quadwp_io_num = -1;
    buscfg.quadhd_io_num = -1;
    buscfg.max_transfer_sz = 240 * 240 * 2 + 8;
    spi_bus_initialize(SPI2_HOST, &buscfg, SPI_DMA_CH_AUTO);

    spi_device_interface_config_t devcfg = {};
    devcfg.clock_speed_hz = 50 * 1000 * 1000;
    devcfg.mode = 0;
    devcfg.spics_io_num = PIN_CS;
    devcfg.queue_size = 7;
    devcfg.flags = SPI_DEVICE_HALFDUPLEX;
    spi_bus_add_device(SPI2_HOST, &devcfg, &spi);

    // Hardware Reset
    gpio_set_level((gpio_num_t)PIN_RST, 0);
    vTaskDelay(pdMS_TO_TICKS(100));
    gpio_set_level((gpio_num_t)PIN_RST, 1);
    vTaskDelay(pdMS_TO_TICKS(100));

    // ST7789 Init Sequence
    sendCmd(0x3A); uint8_t d1[] = {0x05}; sendData(d1, 1);
    sendCmd(0xB2); uint8_t d2[] = {0x0C, 0x0C, 0x00, 0x33, 0x33}; sendData(d2, 5);
    sendCmd(0xB7); uint8_t d3[] = {0x35}; sendData(d3, 1);
    sendCmd(0xBB); uint8_t d4[] = {0x32}; sendData(d4, 1);
    sendCmd(0xC2); uint8_t d5[] = {0x01}; sendData(d5, 1);
    sendCmd(0xC3); uint8_t d6[] = {0x15}; sendData(d6, 1);
    sendCmd(0xC4); uint8_t d7[] = {0x20}; sendData(d7, 1);
    sendCmd(0xC6); uint8_t d8[] = {0x0F}; sendData(d8, 1);
    sendCmd(0xD0); uint8_t d9[] = {0xA4, 0xA1}; sendData(d9, 2);
    sendCmd(0xE0); uint8_t d10[] = {0xD0, 0x08, 0x0E, 0x09, 0x09, 0x05, 0x31, 0x33, 0x48, 0x17, 0x14, 0x15, 0x31, 0x34}; sendData(d10, 14);
    sendCmd(0xE1); uint8_t d11[] = {0xD0, 0x08, 0x0E, 0x09, 0x09, 0x15, 0x31, 0x33, 0x48, 0x17, 0x14, 0x15, 0x31, 0x34}; sendData(d11, 14);
    sendCmd(0x36); uint8_t d12[] = {0x00}; sendData(d12, 1); // Orientation
    sendCmd(0x21); // INVON
    sendCmd(0x11); // SLPOUT
    vTaskDelay(pdMS_TO_TICKS(120));
    sendCmd(0x29); // DISPON
}

void LCD::fill(uint16_t color) {
    fillRect(0, 0, 240, 240, color);
}

void LCD::drawPixel(uint16_t x, uint16_t y, uint16_t color) {
    setAddrWindow(x, y, x, y);
    uint8_t c[2] = {(uint8_t)(color >> 8), (uint8_t)(color & 0xFF)};
    sendData(c, 2);
}

void LCD::drawRect(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color) {
    fillRect(x, y, w, 1, color);
    fillRect(x, y + h - 1, w, 1, color);
    fillRect(x, y, 1, h, color);
    fillRect(x + w - 1, y, 1, h, color);
}

void LCD::fillRect(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color) {
    if(w == 0 || h == 0) return;
    setAddrWindow(x, y, x + w - 1, y + h - 1);
    uint32_t pixels = w * h;
    uint8_t c[2] = {(uint8_t)(color >> 8), (uint8_t)(color & 0xFF)};
    
    const int buf_len = 1024;
    uint8_t* buf = new uint8_t[buf_len * 2];
    for (int i = 0; i < buf_len; i++) {
        buf[i * 2] = c[0];
        buf[i * 2 + 1] = c[1];
    }
    
    gpio_set_level((gpio_num_t)PIN_DC, 1);
    while (pixels > 0) {
        int to_send = (pixels > buf_len) ? buf_len : pixels;
        spi_transaction_t t = {};
        t.length = to_send * 16;
        t.tx_buffer = buf;
        spi_device_transmit(spi, &t);
        pixels -= to_send;
    }
    delete[] buf;
}

void LCD::drawChar(uint16_t x, uint16_t y, char c, uint16_t color, uint16_t bg_color, uint8_t size) {
    if (c < ' ' || c > '~') return;
    uint8_t num = c - ' ';
    uint16_t w = 8 * size;
    uint16_t h = 16 * size;
    uint16_t* buf = new uint16_t[w * h];
    
    for (uint8_t i = 0; i < 16; i++) {
        uint8_t temp = asc2_1608[num][i];
        for (uint8_t j = 0; j < 8; j++) {
            uint16_t pcolor = (temp & 0x80) ? color : bg_color;
            for (uint8_t sy = 0; sy < size; sy++) {
                for (uint8_t sx = 0; sx < size; sx++) {
                    buf[(i * size + sy) * w + (j * size + sx)] = pcolor;
                }
            }
            temp <<= 1;
        }
    }
    
    setAddrWindow(x, y, x + w - 1, y + h - 1);
    gpio_set_level((gpio_num_t)PIN_DC, 1);
    
    uint8_t* tx_buf = new uint8_t[w * h * 2];
    for (int i = 0; i < w * h; i++) {
        tx_buf[i * 2] = buf[i] >> 8;
        tx_buf[i * 2 + 1] = buf[i] & 0xFF;
    }
    
    spi_transaction_t t = {};
    t.length = w * h * 16;
    t.tx_buffer = tx_buf;
    spi_device_transmit(spi, &t);
    
    delete[] buf;
    delete[] tx_buf;
}

void LCD::printStr(const std::string& str, uint16_t x, uint16_t y, uint16_t color, uint16_t bg_color, uint8_t size) {
    uint16_t x0 = x;
    for (char c : str) {
        drawChar(x, y, c, color, bg_color, size);
        x += 8 * size;
        if (x + 8 * size > 240) {
            x = x0;
            y += 16 * size;
        }
    }
}

void LCD::drawCircle(uint16_t x0, uint16_t y0, uint16_t r, uint16_t color) {
    int f = 1 - r;
    int ddF_x = 1;
    int ddF_y = -2 * r;
    int x = 0;
    int y = r;

    drawPixel(x0, y0 + r, color);
    drawPixel(x0, y0 - r, color);
    drawPixel(x0 + r, y0, color);
    drawPixel(x0 - r, y0, color);

    while (x < y) {
        if (f >= 0) {
            y--;
            ddF_y += 2;
            f += ddF_y;
        }
        x++;
        ddF_x += 2;
        f += ddF_x;

        drawPixel(x0 + x, y0 + y, color);
        drawPixel(x0 - x, y0 + y, color);
        drawPixel(x0 + x, y0 - y, color);
        drawPixel(x0 - x, y0 - y, color);
        drawPixel(x0 + y, y0 + x, color);
        drawPixel(x0 - y, y0 + x, color);
        drawPixel(x0 + y, y0 - x, color);
        drawPixel(x0 - y, y0 - x, color);
    }
}

void LCD::fillCircle(uint16_t x0, uint16_t y0, uint16_t r, uint16_t color) {
    for (int y = -r; y <= r; y++) {
        for (int x = -r; x <= r; x++) {
            if (x * x + y * y <= r * r) {
                drawPixel(x0 + x, y0 + y, color);
            }
        }
    }
}
