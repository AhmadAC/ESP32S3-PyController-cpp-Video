LCD::LCD() : spi_handle_(nullptr) {
}

void LCD::reset() {
    gpio_set_level(PIN_NUM_RST, 0);
    vTaskDelay(pdMS_TO_TICKS(100));
    gpio_set_level(PIN_NUM_RST, 1);
    vTaskDelay(pdMS_TO_TICKS(100));
}

void LCD::send_cmd(uint8_t cmd) {
    gpio_set_level(PIN_NUM_DC, 0);
    spi_transaction_t t = {};
    t.length = 8;
    t.tx_buffer = &cmd;
    spi_device_polling_transmit(spi_handle_, &t);
}

void LCD::send_data(const uint8_t* data, int len) {
    if (len == 0) return;
    gpio_set_level(PIN_NUM_DC, 1);
    spi_transaction_t t = {};
    t.length = len * 8;
    t.tx_buffer = data;
    spi_device_polling_transmit(spi_handle_, &t);
}

void LCD::set_address_window(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2) {
    x1 += X_OFFSET;
    x2 += X_OFFSET;
    y1 += Y_OFFSET;
    y2 += Y_OFFSET;

    send_cmd(0x2A); // Column Address Set
    uint8_t caset[4] = { (uint8_t)(x1 >> 8), (uint8_t)(x1 & 0xFF), (uint8_t)(x2 >> 8), (uint8_t)(x2 & 0xFF) };
    send_data(caset, 4);

    send_cmd(0x2B); // Row Address Set
    uint8_t raset[4] = { (uint8_t)(y1 >> 8), (uint8_t)(y1 & 0xFF), (uint8_t)(y2 >> 8), (uint8_t)(y2 & 0xFF) };
    send_data(raset, 4);

    send_cmd(0x2C); // Memory Write
}

